class A 
{
	public static void main(String[] args) 
	{
		//A whole number without any decimal point
		System.out.println(1000);
		//A number with decimal
		System.out.println(90.0);
		//A single character(any character) within a pair of single quote
		System.out.println('a');
		//A sequence of character(any no.s) within a pair of double quotes
		System.out.println("abc");
		//In case of boolean literal either it can be true or false
		System.out.println(true);
		//Null can't be printed directly in SOP we need to print it via a refernce
		//A a1 = null;
		//args = null;
		System.out.println("null");
	}
}
